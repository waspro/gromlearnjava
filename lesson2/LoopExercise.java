package lesson2;

public class LoopExercise {
    public static void main(String[] args) {
        // 5-10 15-20
        int a = 5;
        while (a <= 10){
            System.out.println(a);
            a++;
        }

        a = 15;
        while (a <= 20){
            System.out.println(a);
            a++;
        }
    }
}
